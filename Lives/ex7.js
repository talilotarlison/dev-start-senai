function tamanhoPalavra(palavra){
    return palavra.length
}

function palavraMaisculo(palavra){
    return palavra.toLowerCase()
}

function palvraMinusculo(palavra){
    return palavra.toUpperCase()
}

console.log(tamanhoPalavra("carro"))
console.log(palavraMaisculo("Carro"))
console.log(palvraMinusculo("Carro"))